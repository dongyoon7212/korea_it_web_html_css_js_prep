//해결 방안으로 비동기식 처리를 하여야한다
//예를 들어 서버에 데이터 요청을 보내놓고 작업 2와 작업 3을 수행한다
//이때 서버에서 데이터를 받아오면 그때 콜백함수나 그에 따른 작업을 수행하면 된다

//대표적으로 비동기 함수 중 하나인 setTimeout
console.log("작업 1");
setTimeout(() => {
	//setTimeout은 첫번째 인자로 콜백함수, 두번째 인자는 지연시간(밀리초)
	console.log("작업 2");
}, 3000);

setTimeout(() => {
	//setTimeout은 첫번째 인자로 콜백함수, 두번째 인자는 지연시간(밀리초)
	console.log("작업 2");
}, 3000);
console.log("작업 1");

